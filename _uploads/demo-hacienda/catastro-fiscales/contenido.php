<p class="fs-5 py-2">
    La "Dirección Provincial de Catastro y Tierras Fiscales" es un organismo encargado de gestionar la información relacionada con la propiedad de tierras y bienes inmuebles.
</p>
<ul class="fs-5 py-2">
    <li>Mantiene y actualiza el registro de la propiedad inmueble en la provincia, que incluye detalles sobre la ubicación, dimensiones y características de los terrenos y edificaciones.</li>
    <li>Realiza tasaciones y valoraciones de bienes inmuebles para diversos fines, como la determinación de impuestos, la planificación urbana y la gestión de recursos.</li>
    <li>Administra las tierras de propiedad del Estado provincial, incluyendo su asignación, venta y conservación.</li>
    <li>Desarrolla y aplica regulaciones relacionadas con el uso y la gestión de la tierra, asegurando que se cumplan las normativas urbanísticas y rurales.</li>
    <li>Ofrece servicios de asesoramiento a ciudadanos y entidades sobre temas catastrales, como la obtención de certificados catastrales, la regularización de propiedades y la consulta sobre planos y registros.</li>
    <li>Colabora con otras instituciones en la planificación del uso del suelo, tanto en áreas urbanas como rurales, para promover un desarrollo ordenado y sostenible.</li>
    <li>Gestiona y resuelve conflictos relacionados con la propiedad de tierras y bienes inmuebles, proporcionando información y asistencia en casos de disputas.</li>
</ul>

