<p class="fs-5 py-2">
    La "Dirección de Compras y Contrataciones" es una entidad clave en la administración pública y en algunas organizaciones privadas, encargada de gestionar y supervisar el proceso de adquisiciones y contratos.
</p>
<ul class="fs-5 py-2">
    <li>Administra la compra de bienes y servicios necesarios para el funcionamiento de la entidad, asegurando que las adquisiciones se realicen de manera eficiente, económica y conforme a la normativa vigente.</li>
    <li>Coordina y supervisa los procesos de contratación de proveedores y prestadores de servicios. Esto incluye la elaboración de pliegos de condiciones, la recepción y evaluación de ofertas, y la adjudicación de contratos.</li>
    <li>Asegura que las adquisiciones y contrataciones cumplan con las leyes y regulaciones pertinentes, incluyendo la normativa de contratación pública y las políticas internas de la organización.</li>
    <li>Monitorea el cumplimiento de los contratos y asegura que los proveedores cumplan con los términos acordados. Esto incluye la gestión de cambios en los contratos y la resolución de conflictos que puedan surgir.</li>
    <li>Promueve la transparencia en el proceso de compras y contrataciones, garantizando que se realicen de manera justa y abierta. Esto puede incluir la publicación de licitaciones y adjudicaciones y la realización de auditorías para verificar la correcta aplicación de los recursos.</li>
    <li>Busca maneras de optimizar el gasto público mediante la planificación estratégica de adquisiciones, la negociación de mejores condiciones con proveedores, y la identificación de oportunidades para la mejora continua en los procesos de compras y contrataciones.</li>
    <li>Ofrece asesoramiento a otras áreas de la organización sobre cuestiones relacionadas con compras y contrataciones y proporciona capacitación al personal para asegurar el cumplimiento de las mejores prácticas y procedimientos.</li>
    <li>Desarrolla y actualiza políticas y procedimientos relacionados con las compras y contrataciones para asegurar que se ajusten a las mejores prácticas y a los requisitos legales.</li>
</ul>


